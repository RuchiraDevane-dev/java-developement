import java.util.*;
public class Switchcase {
    public static void main(String args[]) {
        int number =2;
        switch (number) {
            case 1 :System.out.println("Samosa");
            break;
            case 2 :System.out.println("Waffle");
            break;
            case 3 :System.out.println("Coffee");
            break;
            default :System.out.println("kuch nahi milega");
        }
    }
}
